meters = float(input('Type a distance in meters: '))
kilometer = meters / 1000
hectometer = kilometer * 10
decameter = hectometer * 10
decimeter = meters * 10
centimeter = decimeter * 10
milimeter = centimeter * 10

print('Kilometers = {}\nHectometers = {}\nDecameters = {}\nMeters = {}\nDecimeters = {}\nCentimeters = {}\nMilimeters = {}'.format(kilometer,hectometer,decameter,meters,decimeter,centimeter,milimeter))